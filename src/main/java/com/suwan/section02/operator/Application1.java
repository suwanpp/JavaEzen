package com.suwan.section02.operator;

public class Application1 {

    public static void main(String[] args) {


        int a = 23, b = 20;
        int result = (a>b)? a:b;
        System.out.println("두 수 중 큰 수는 " + result + "입니다.");
        /* 두 개의 정수형 변수를 선언하고, 삼항 연산자를 사용하여
         * 두 수 중 큰 수를 출력하는 프로그램을 작성해본다.
         *
         *
         * -- 출력 예시 --
         *
         * 두 수 중 큰 수는 20입니다.
         *
         * */
    }

}